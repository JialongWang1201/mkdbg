const Vm32Scenario *vm32_scenario_find(const char *name)
{
  if (name == NULL) {
    return NULL;
  }
  for (size_t i = 0; i < (sizeof(k_vm32_scenarios) / sizeof(k_vm32_scenarios[0])); ++i) {
    if (strcmp(name, k_vm32_scenarios[i].name) == 0) {
      return &k_vm32_scenarios[i];
    }
  }
  return NULL;
}

void vm32_scenario_list(void (*write)(const char *))
{
  if (write == NULL) {
    return;
  }
  write("vm scenarios:\r\n");
  for (size_t i = 0; i < (sizeof(k_vm32_scenarios) / sizeof(k_vm32_scenarios[0])); ++i) {
    char buf[96];
    snprintf(buf, sizeof(buf), "- %s: %s\r\n", k_vm32_scenarios[i].name, k_vm32_scenarios[i].desc);
    write(buf);
  }
}

Vm32Result vm32_scenario_load(Vm32 *vm, const Vm32Scenario *scenario)
{
  if (vm == NULL || scenario == NULL) {
    return VM32_ERR_MEM;
  }
  if (scenario->program == NULL || scenario->size == 0U) {
    return VM32_OK;
  }
  if (scenario->size > VM32_MEM_SIZE) {
    return VM32_ERR_MEM;
  }
  memcpy(vm->mem, scenario->program, scenario->size);
  return VM32_OK;
}
